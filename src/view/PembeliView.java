    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Apakah yakin gin menghapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        switch(getAnswer){
            case 0:
                //jika jawaban user adalah YES
                try{
                    pc.deleteDataPembeli(selectedId);
                    clearText();
                    showPembeli();
                }catch(Exception e){
                    System.out.println("Error : " + e.getMessage());
                }
                break;
            
            case 1:
                //jika jawaban user adalah NO
                break;
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        setEditDeleteBtn(false);
        //Ketika button Cari diklik maka akan menonaktifkan component input
        setComponent(false);
        
        try{
            TablePembeli p = pc.showDataPembeli(searchInput.getText());
            if (p.getRowCount() == 0) {
                //Ketika data yang dicari tidak ditemukan
                clearText();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data tidak ditemukan", "Konfirmasi", JOptionPane.DEFAULT_OPTION);
            } else {
                //Ketika data yang dicari ditemukan
                tablePembeli.setModel(p);
            }
            // Ketika button cari diklik maka kan menghapus seluruh isi input
            clearText();
        }catch(Exception e){
            System.out.println("Error : " + e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

