    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        try{
            Pembeli p = new Pembeli(namaInput.getText(), alamatInput.getText(), noTeleponInput.getText());
            if(action.equals("Tambah")){
                //Ketika action berstatus tambah maka akan memanggil fungsi insert
                pc.insertPembeli(p);
            }else{
                //Ketika action berstatus ubah maka akan memanggil fungsi update
                pc.updateDataPembeli(p, selectedId);
            }
            clearText();
            showPembeli();
            setComponent(false);
            setEditDeleteBtn(false);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void tablePembeliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePembeliMouseClicked
        // TODO add your handling code here:
        setEditDeleteBtn(true);

        setComponent(false);

        int clickedRow = tablePembeli.getSelectedRow();
        TableModel tableModel = tablePembeli.getModel();

        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 0).toString());
        namaInput.setText(tableModel.getValueAt(clickedRow, 1).toString());
        alamatInput.setText(tableModel.getValueAt(clickedRow, 2).toString());
        noTeleponInput.setText(tableModel.getValueAt(clickedRow, 3).toString());
    }//GEN-LAST:event_tablePembeliMouseClicked

