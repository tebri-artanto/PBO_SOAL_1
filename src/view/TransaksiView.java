    public void setComputerToDropdown() {
        listComputer = cc.showListAllComputer();
        for (int i = 0; i < listComputer.size(); i++) {
            namaComputerDropdown.addItem(listComputer.get(i));
        }
    }
    
    public void setPembeliToDropdown() {
        listPembeli = pc.showListPembeli();
        for (int i = 0; i < listPembeli.size(); i++) {
            namaPembeliDropdown.addItem(listPembeli.get(i));
        }
    }
    
