    // Method untuk memeriksa input kosong
    public void inputKosongException() throws InputKosongException {
        if (tanggalTransaksiInput.getText().isEmpty() || totalHargaInput.getText().isEmpty()
                 || namaComputerDropdown.getSelectedIndex() == -1 || namaPembeliDropdown.getSelectedIndex() == -1) {
            throw new InputKosongException();
        }
       
    }

