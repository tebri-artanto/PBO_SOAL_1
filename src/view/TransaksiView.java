    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        setEditDeleteBtn(false);
        
        setComponent(false);

        try {
            TableTransaksi tt = tc.showTransaksi(searchInput.getText());
            if (tt.getRowCount() == 0) {
                //Ketika data yang dicari tidak ditemukan
                clearText();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data tidak ditemukan", "Konfirmasi", JOptionPane.DEFAULT_OPTION);
            } else {
                //Ketika data yang dicari ditemukan
                tableTransaksi.setModel(tt);
            }
            // Ketika button cari diklik maka kan menghapus seluruh isi input
            clearText();
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String bonus = "";
        
        try {
            inputKosongException();
           // String kelas = "";
           // String durasi_iklan = null;
            int namaComputerIndex = namaComputerDropdown.getSelectedIndex();
            int namaPembeliIndex = namaPembeliDropdown.getSelectedIndex();
            Computer selectedComputer = listComputer.get(namaComputerIndex);
            Pembeli selectedPembeli = listPembeli.get(namaPembeliIndex);
            
            if (mouseCheckbox.isSelected()) {
                bonus = bonus + "Mouse";
            } 
            if (tasCheckbox.isSelected()) {
                bonus =  bonus + "Tas";
            } 
            if (mousepadCheckbox.isSelected()){
                bonus = bonus + "Mousepad";
            }
            
            if(bonus.equalsIgnoreCase("")){
                bonus = "-,-,-";
            }else if(bonus.equalsIgnoreCase("Mouse")){
                bonus = "Mouse,-,-";
            }else if(bonus.equalsIgnoreCase("Tas")){
                bonus = "-,Tas,-";
            }else if(bonus.equalsIgnoreCase("Mousepad")){
                bonus = "-,-,Mousepad";
            }else if(bonus.equalsIgnoreCase("MouseTas")){
                bonus = "Mouse,Tas,-";
            }else if(bonus.equalsIgnoreCase("TasMousepad")){
                bonus = "-,Tas,Mousepad";
            }else if(bonus.equalsIgnoreCase("MouseMousepad")){
                bonus = "Mouse,-,Mousepad";
            }else if(bonus.equalsIgnoreCase("MouseTasMousepad")){
                bonus = "Mouse,Tas,Mousepad";
            }
            
            if (action.equals("Tambah")) {
                //Ketika action berstatus tambah maka akan memanggil fungsi insert
                Transaksi t = new Transaksi(tanggalTransaksiInput.getText(), 
                        totalHargaInput.getText(), bonus, selectedComputer, selectedPembeli);
                tc.insertDataTransaksi(t);
            } else {
                //Ketika action berstatus ubah maka akan memanggil fungsi update
                Transaksi t = new Transaksi(selectedId, tanggalTransaksiInput.getText(), 
                        totalHargaInput.getText(), bonus, selectedComputer, selectedPembeli);
                tc.updateDataTransaksi(t);
            }
            clearText();
            showTransaksi();
            setComponent(false);

            setEditDeleteBtn(false);
        } catch (InputKosongException e) {
            JOptionPane.showMessageDialog(this, e.message());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void tableTransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTransaksiMouseClicked
        // TODO add your handling code here:
        int indexComputer = -1;
        int indexPembeli = -1;
        setEditDeleteBtn(true);

        setComponent(false);

        int clickedRow = tableTransaksi.getSelectedRow();
        TableModel tableModel = tableTransaksi.getModel();

        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 8).toString());
        tanggalTransaksiInput.setText(tableModel.getValueAt(clickedRow, 3).toString());
        totalHargaInput.setText(tableModel.getValueAt(clickedRow, 4).toString());

        String id_computer = tableModel.getValueAt(clickedRow, 9).toString();
        for(Computer c : listComputer) {
            if (c.getId().equals(id_computer)) {
                indexComputer = listComputer.indexOf(c);
            }
        }
        namaComputerDropdown.setSelectedIndex(indexComputer);
        
        String temp = tableModel.getValueAt(clickedRow, 10).toString();
        Integer id_Pembeli = Integer.parseInt(temp);
        for(Pembeli p : listPembeli) {
            if (p.getId() == id_Pembeli) {
                indexPembeli = listPembeli.indexOf(p);
            }
        }
        namaPembeliDropdown.setSelectedIndex(indexPembeli);
        
        String bonus = tableModel.getValueAt(clickedRow, 5).toString();
        if(bonus.equalsIgnoreCase("-,-,-")){
            mouseCheckbox.setSelected(false);
            tasCheckbox.setSelected(false);
            mousepadCheckbox.setSelected(false);
        }else if(bonus.equalsIgnoreCase("Mouse,-,-")){
            mouseCheckbox.setSelected(true);
            tasCheckbox.setSelected(false);
            mousepadCheckbox.setSelected(false);
        }else if(bonus.equalsIgnoreCase("-,Tas,-")){
            mouseCheckbox.setSelected(false);
            tasCheckbox.setSelected(true);
            mousepadCheckbox.setSelected(false);
        }else if(bonus.equalsIgnoreCase("-,-,Mousepad")){
            mouseCheckbox.setSelected(false);
            tasCheckbox.setSelected(false);
            mousepadCheckbox.setSelected(true);
        }else if(bonus.equalsIgnoreCase("Mouse,Tas,-")){
            mouseCheckbox.setSelected(true);
            tasCheckbox.setSelected(true);
            mousepadCheckbox.setSelected(false);
        }else if(bonus.equalsIgnoreCase("-,Tas,Mousepad")){
            mouseCheckbox.setSelected(false);
            tasCheckbox.setSelected(true);
            mousepadCheckbox.setSelected(true);
        }else if(bonus.equalsIgnoreCase("Mouse,-,Mousepad")){
            mouseCheckbox.setSelected(true);
            tasCheckbox.setSelected(false);
            mousepadCheckbox.setSelected(true);
        }else if(bonus.equalsIgnoreCase("Mouse,Tas,Mousepad")){
            mouseCheckbox.setSelected(true);
            tasCheckbox.setSelected(true);
            mousepadCheckbox.setSelected(true);
        }
    }//GEN-LAST:event_tableTransaksiMouseClicked

