    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            inputKosongException();
            idException();
            noCompSelected();
            
            if (action.equals("Tambah")) {
                if(jRadioButtonDesktop.isSelected() == true) {
                    try {
                        desktopKosongException();
                        c = new Computer(idInput.getText(),
                                namaInput.getText(),
                                processorInput.getText(), 
                                Integer.parseInt(ramInput.getText()), 
                                d,
                                Integer.parseInt(desktopInput.getText()),
                                0);
                        computerControl.insertDataComputer(c);
                        JOptionPane.showMessageDialog(this,
                            "Data Computer Desktop berhasil ditambah!");
                    } 
                    catch (DesktopKosongException e) 
                    {
                        JOptionPane.showMessageDialog(this, e.message());
                    }
                }
                else if(jRadioButtonLaptop.isSelected() == true) {
                    try {
                        laptopKosongException();
                        c = new Computer(idInput.getText(),
                                namaInput.getText(),
                                processorInput.getText(), 
                                Integer.parseInt(ramInput.getText()), 
                                l,
                                0,
                                Integer.parseInt(laptopInput.getText()));
                        computerControl.insertDataComputer(c);
                        JOptionPane.showMessageDialog(this,
                            "Data Laptop berhasil ditambah!");
                    }
                    catch (LaptopKosongException e) 
                    {
                        JOptionPane.showMessageDialog(this, e.message());
                    }
                }
            }
            else if(action.equals("ubahDesktop")) {
                c = new Computer(null,
                            namaInput.getText(),
                            processorInput.getText(), 
                            Integer.parseInt(ramInput.getText()), 
                            d,
                            Integer.parseInt(desktopInput.getText()),
                            0);
                    computerControl.updateDataComputer(c, searchInput.getText());
                    JOptionPane.showMessageDialog(this,
                        "Data Computer Desktop berhasil diedit!");
            }
            else if(action.equals("ubahLaptop")) {
                c = new Computer(null,
                            namaInput.getText(),
                            processorInput.getText(), 
                            Integer.parseInt(ramInput.getText()), 
                            d,
                            0,
                            Integer.parseInt(laptopInput.getText()));
                    computerControl.updateDataComputer(c, searchInput.getText());
                    JOptionPane.showMessageDialog(this,
                        "Data Computer Laptop berhasil diedit!");
            }
            clearText();
            showData();
            setComponent(false);
            setDesktopComponent(false);
            setLaptopComponent(false);
            setEditDeleteBtn(false);
            buttonGroupComputer.clearSelection();
            action = null;
        } catch (InputKosongException e) {
            JOptionPane.showMessageDialog(this, e.message());
        } catch (IdException e) {
            JOptionPane.showMessageDialog(this, e.message());
        } catch (NoCompSelectedException e) {
            JOptionPane.showMessageDialog(this, e.message());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Input RAM / Daya Listrik / Baterai bukan angka!");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

