    public void inputKosongException() throws InputKosongException {
        if (idInput.getText().isEmpty() || namaInput.getText().isEmpty()
                || processorInput.getText().isEmpty()
                || ramInput.getText().isEmpty()) {
            throw new InputKosongException();
        }
    }
    
    public void desktopKosongException() throws DesktopKosongException {
        if(desktopInput.getText().isEmpty())
        {
            throw new DesktopKosongException();
        }
    }
    
    public void laptopKosongException() throws LaptopKosongException {
        if(laptopInput.getText().isEmpty())
        {
            throw new LaptopKosongException();
        }
    }
    
    public void idException() throws IdException {
        if (idInput.getText().length() > 8 && idInput.getText().length() < 5) {
            throw new IdException();
        }
    }
    
    public void noCompSelected() throws NoCompSelectedException {
        if (jRadioButtonDesktop.isSelected() == false && jRadioButtonLaptop.isSelected() == false)
        {
            throw new NoCompSelectedException();
        }
    }
    
    public String generateDesktopID()
    {
        String lastId = null;
        String tempId = null;
        String generatedId = null;
        int numberId;
        
        lastId = computerControl.getLastDesktopId();
        tempId = lastId.substring(4);
        numberId = Integer.parseInt(tempId) + 1;
        generatedId = "DES-" + String.valueOf(numberId);
        
        return generatedId;
    }
    
    public String generateLaptopID()
    {
        String lastId = null;
        String tempId = null;
        String generatedId = null;
        int numberId;
        
        lastId = computerControl.getLastLaptopId();
        tempId = lastId.substring(4);
        numberId = Integer.parseInt(tempId) + 1;
        generatedId = "LAP-" + String.valueOf(numberId);
        
        return generatedId;
    }
