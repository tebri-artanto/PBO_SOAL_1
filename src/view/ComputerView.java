    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int getAnswer = JOptionPane.showConfirmDialog(rootPane, 
                "Apakah yakin ingin menghapus data ?", "Konfirmasi",
                JOptionPane.YES_NO_OPTION);
        if (getAnswer == JOptionPane.YES_OPTION ){
            try {
                if(action.equalsIgnoreCase("ubahDesktop") || action.equalsIgnoreCase("ubahLaptop")) {
                    computerControl.deleteDataComputer(idInput.getText());
                    JOptionPane.showMessageDialog(null, "Data Computer berhasil dihapus!");
                }
                clearText();
                buttonGroupComputer.clearSelection();
                action = null;
                showData();
            } catch (Exception e) {
                System.out.println("Error : " + e.getMessage());
            }
        }
        else{JOptionPane.showMessageDialog(null, "Data tidak jadi dihapus!");}
    }//GEN-LAST:event_deleteBtnActionPerformed

