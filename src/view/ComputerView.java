    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        clearText();
        setEditDeleteBtn(true);
        setComponent(false);
        setDesktopComponent(false);
        setLaptopComponent(false);
        buttonGroupComputer.clearSelection();

        try {
            c = computerControl.searchComputer(searchInput.getText());
            if(c == null)
            {
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Computer tidak ditemukan!", 
                        "Konfirmasi", JOptionPane.DEFAULT_OPTION); 
            }
            else if(c.getJenis().equalsIgnoreCase(d)) {
                action = "ubahDesktop";
                
                idInput.setText(c.getId());
                namaInput.setText(c.getNama());
                processorInput.setText(c.getProcessor());
                ramInput.setText(String.valueOf(c.getKapasitasRAM()));
                desktopInput.setText(String.valueOf(c.getDayaListrik()));
                jRadioButtonDesktop.setSelected(true);
            }
            else if(c.getJenis().equalsIgnoreCase(l)) {
                action = "ubahLaptop";
                
                idInput.setText(c.getId());
                namaInput.setText(c.getNama());
                processorInput.setText(c.getProcessor());
                ramInput.setText(String.valueOf(c.getKapasitasRAM()));
                laptopInput.setText(String.valueOf(c.getKapasitasBaterai()));
                jRadioButtonLaptop.setSelected(true);
            }
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

